function capitalizeWords(str) {
  let result = "";
  for (let i = 0; i < str.length; i++) { // –ø—Ä–æ–≤–µ—Ä—è–µ–º —É—Å–ª–æ–≤–∏–µ: –ª–∏–±–æ –Ω–∞—Ö–æ–¥–∏–º—Å—è –Ω–∞ –ø–µ—Ä–≤–æ–º —Å–∏–º–≤–æ–ª–µ (i===0),
    if(i === 0 || str[i - 1] === " ") { //  –ª–∏–±–æ –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Å–∏–º–≤–æ–ª (str[i - 1])- —ç—Ç–æ –ø—Ä–æ–±–µ–ª. –ü–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –∏–Ω–¥–µ–∫—Å–∞ [0], –≤—Å–µ –æ—Å—Ç–∞–ª—å–Ω—ã–µ —Å –ø—Ä–æ–±–µ–ª–∞
      result += str[i].toUpperCase();
    } else {
      result += str[i]
    }
  }
  return result;
}
